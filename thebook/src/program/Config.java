package program;

public class Config {
	private String inputDir;
	private String outputDir;
	private String emojiDir;
	private String imagePoolDir;
	private String debugDir;
	
	public String getInputDir() {
		return inputDir;
	}
	public void setInputDir(String inputDir) {
		this.inputDir = inputDir;
	}
	public String getOutputDir() {
		return outputDir;
	}
	public void setOutputDir(String outputDir) {
		this.outputDir = outputDir;
	}
	public String getEmojiDir() {
		return emojiDir;
	}
	public void setEmojiDir(String emojiDir) {
		this.emojiDir = emojiDir;
	}
	public String getImagePoolDir() {
		return imagePoolDir;
	}
	public void setImagePoolDir(String imagePoolDir) {
		this.imagePoolDir = imagePoolDir;
	}
	public String getDebugDir() {
		return debugDir;
	}
	public void setDebugDir(String debugDir) {
		this.debugDir = debugDir;
	}
}
